<?php
namespace tglobally\tg_nomina\controllers;

use gamboamartin\errores\errores;
use PDO;
use stdClass;
use tglobally\template_tg\html;
use tglobally\tg_nomina\models\nom_nomina;

class controlador_nom_nomina extends \gamboamartin\nomina\controllers\controlador_nom_nomina {

    public string $link_crea_nomina = '';
    public array $sidebar = array();
    public function __construct(PDO $link, stdClass $paths_conf = new stdClass()){
        $html_base = new html();
        parent::__construct( link: $link, html: $html_base);

        $this->modelo = new nom_nomina($this->link);

        $this->seccion_titulo = 'Nominas';
        $this->titulo_pagina = "Nominas";
        $this->titulo_accion = "Nominas";

        $hd = "index.php?seccion=nom_nomina&accion=crea_nomina&session_id=$this->session_id";
        $this->link_crea_nomina = $hd;

        $acciones = $this->define_acciones_menu(acciones: array("alta" => $this->link_crea_nomina));
        if (errores::$error) {
            $error = $this->errores->error(mensaje: 'Error al integrar acciones para el menu', data: $acciones);
            print_r($error);
            die('Error');
        }

        $this->params_actions = new stdClass();
        $this->params_actions->modifica = new stdClass();
        $this->params_actions->modifica->em_registro_patronal_id = new stdClass();
        $this->params_actions->modifica->em_registro_patronal_id->cols = 8;

        $this->params_actions->modifica->em_empleado_id = new stdClass();
        $this->params_actions->modifica->em_empleado_id->cols = 12;

        $this->lista_get_data = true;
    }

    protected function init_datatable(): stdClass
    {
        $columns = array();
        $columns['nom_nomina_id']['titulo'] = 'Id';
        $columns['em_empleado_rfc']['titulo'] = 'RFC';
        $columns['em_empleado_nombre']['titulo'] = 'Nombre';
        $columns['em_empleado_ap']['titulo'] = 'AP';
        $columns['em_empleado_am']['titulo'] = 'AM';
        $columns['nom_nomina_fecha_inicial_pago']['titulo'] = 'F Ini';
        $columns['nom_nomina_fecha_final_pago']['titulo'] = 'F Ini';
        $columns['nom_nomina_fecha_pago']['titulo'] = 'F Pago';
        $columns['nom_nomina_total_percepcion_total']['titulo'] = 'Percepcion Total';
        $columns['nom_nomina_total_otro_pago_total']['titulo'] = 'Otro Pago Total';
        $columns['nom_nomina_total_deduccion_total']['titulo'] = 'Deduccion Total';
        $columns['nom_nomina_total']['titulo'] = 'Total';
        $columns['cat_sat_tipo_nomina_descripcion']['titulo'] = 'Tipo Nom';
        $columns['org_empresa_rfc']['titulo'] = 'RFC Empresa';

        $filtro = array("em_empleado.rfc", "em_empleado.nombre", "nom_nomina.fecha_inicial_pago", "nom_nomina.fecha_final_pago",
            "nom_nomina.fecha_pago", "nom_periodo.codigo", "cat_sat_tipo_nomina.descripcion", "org_empresa.rfc");

        $datatables = new stdClass();
        $datatables->columns = $columns;
        $datatables->filtro = $filtro;
        $datatables->menu_active = true;

        return $datatables;
    }

    public function lista(bool $header, bool $ws = false): array
    {

        $r_lista = parent::lista(header:false); // TODO: Change the autogenerated stub
        if(errores::$error){
            return $this->retorno_error(mensaje: 'Error al ejecutar template',data:  $r_lista, header: $header,ws:$ws);
        }

        $links = $this->registros;

        $registros = $this->modelo->registros(return_obj: true);
        if(errores::$error){
            return $this->retorno_error(mensaje: 'Error al obtener registros',data:  $registros, header: $header,ws:$ws);
        }

        foreach ($registros as $indice=>$registro){

            $link_genera_xml = $this->obj_link->link_con_id(accion:'genera_xml',link: $this->link,
                registro_id:  $registro->nom_nomina_id, seccion:  $this->tabla);
            if(errores::$error){
                return $this->errores->error(mensaje: 'Error al genera link',data:  $link_genera_xml);
            }
            $registro->link_genera_xml = $link_genera_xml;

            $link_descarga_recibo_nomina = $this->obj_link->link_con_id(accion:'descarga_recibo_nomina',link: $this->link,
                registro_id:  $registro->nom_nomina_id, seccion:  $this->tabla);
            if(errores::$error){
                return $this->errores->error(mensaje: 'Error al genera link',data:  $link_descarga_recibo_nomina);
            }

            $registro->link_descarga_recibo_nomina = $link_descarga_recibo_nomina;

            foreach($links as $link){

                if((int)$registro->nom_nomina_id  === (int)$link->nom_nomina_id){
                    $registro->razon_social = $registro->em_empleado_nombre.' '. $registro->em_empleado_ap.' ';
                    $registro->razon_social .= $registro->em_empleado_am;
                    $registro->total_cuota_patronal = $registro->nom_nomina_total_cuota;
                    $registro->link_modifica = $link->link_modifica;
                    $registro->link_elimina_bd = $link->link_elimina_bd;
                }
            }
            $registros[$indice] = $registro;

        }

        $this->registros = $registros;

        return $r_lista;
    }

    public function menu_item(string $menu_item_titulo, string $link, bool $menu_seccion_active = false,bool $menu_lateral_active = false): array
    {
        $menu_item = array();
        $menu_item['menu_item'] = $menu_item_titulo;
        $menu_item['menu_seccion_active'] = $menu_seccion_active;
        $menu_item['link'] = $link;
        $menu_item['menu_lateral_active'] = $menu_lateral_active;

        return $menu_item;
    }

}
